package modul_1.latihan;

public class DataDiri {
    public static void main(String[] args) {

        // Deklarasi variabel
        String namaLengkap = "Nasywa Nurshabira";
        String tempatLahir = "Bireuen";
        String tanggalLahir = "31 Mei 2005";
        char golonganDarah = 'O';
        int umur = 20;
        double tinggiBadan = 149.0; // dalam cm
        char jenisKelamin = 'P'; // L = Laki-laki, P = Perempuan
        String agama = "Islam";
        String pekerjaan = "Mahasiswa";

        // Menampilkan data
        System.out.println("===== DATA DIRI =====");
        System.out.println("Nama Lengkap   : " + namaLengkap);
        System.out.println("Tempat Lahir   : " + tempatLahir);
        System.out.println("Tanggal Lahir  : " + tanggalLahir);
        System.out.println("Golongan Darah : " + golonganDarah);
        System.out.println("Umur           : " + umur + " tahun");
        System.out.println("Tinggi Badan   : " + tinggiBadan + " cm");
        System.out.println("Jenis Kelamin  : " + jenisKelamin);
        System.out.println("Agama          : " + agama);
        System.out.println("Pekerjaan      : " + pekerjaan);
    }
}
